    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
    String kategoriTerpilih = jComboBox1.getSelectedItem().toString();
    String namaMenu = jTextField1.getText();
    String harga = jTextField2.getText();
    String stok = jTextField3.getText();
    
    if (idMenuTerpilih.equals("") || kategoriTerpilih.equals("- Pilih Kategori -") || namaMenu.equals("") || harga.equals("") || stok.equals("")) {
        JOptionPane.showMessageDialog(null, "Pilih data di tabel dan pastikan form tidak kosong!");
        return;
    }
    
    java.sql.Connection conn = Koneksi.configDB();
    java.sql.Statement st = conn.createStatement();
    
    // Cari id_kategori yang baru berdasarkan nama kategori di dropdown
    String sqlCariKategori = "SELECT id_kategori FROM tb_kategori WHERE nama_kategori = '" + kategoriTerpilih + "'";
    java.sql.ResultSet rs = st.executeQuery(sqlCariKategori);
    
    String idKategori = "";
    if (rs.next()) {
        idKategori = rs.getString("id_kategori");
    }
    
    // Eksekusi proses Update
    String sqlUpdate = "UPDATE tb_menu SET id_kategori = '" + idKategori + "', nama_menu = '" + namaMenu + "', harga = '" + harga + "', stok = '" + stok + "' WHERE id_menu = '" + idMenuTerpilih + "'";
    st.executeUpdate(sqlUpdate);
    
    JOptionPane.showMessageDialog(null, "Data Menu Berhasil Diubah!");
    tampilkanData(); // Refresh JTable
    jButton4.doClick(); // Panggil tombol Reset
    
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error Edit: " + e.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
    if (idMenuTerpilih.equals("")) {
        JOptionPane.showMessageDialog(null, "Silakan pilih data pada tabel yang ingin dihapus!");
        return;
    }
    
    int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus menu ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    
    if (konfirmasi == JOptionPane.YES_OPTION) {
        String sqlDelete = "DELETE FROM tb_menu WHERE id_menu = '" + idMenuTerpilih + "'";
        java.sql.Connection conn = Koneksi.configDB();
        java.sql.Statement st = conn.createStatement();
        st.executeUpdate(sqlDelete);
        
        JOptionPane.showMessageDialog(null, "Data Menu Berhasil Dihapus!");
        tampilkanData(); // Refresh JTable
        jButton4.doClick(); // Panggil tombol Reset
    }
    
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error Hapus: " + e.getMessage());
}
    }//GEN-LAST:event_jButton3ActionPerformed

