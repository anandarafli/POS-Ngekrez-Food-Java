    // Fungsi 1: Menampilkan daftar kategori dari tb_kategori ke ComboBox
private void tampilKategori() {
    try {
        Connection conn = Koneksi.configDB();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM tb_kategori");
        
        jComboBox1.removeAllItems();
        jComboBox1.addItem("- Pilih Kategori -");
        
        while (rs.next()) {
            // Memasukkan nama kategori ke dalam dropdown
            jComboBox1.addItem(rs.getString("nama_kategori")); 
        }
    } catch (Exception e) {
        System.out.println("Error tampil kategori: " + e.getMessage());
    }
}

// Fungsi 2: Menampilkan daftar menu dari tb_menu ke JTable
private void tampilkanData() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("No");
    model.addColumn("ID Menu");
    model.addColumn("Kategori"); 
    model.addColumn("Nama Menu");
    model.addColumn("Harga");
    model.addColumn("Stok");
    
    try {
        int no = 1;
        // Menggunakan teknik JOIN untuk menggabungkan tb_menu dan tb_kategori
        String sql = "SELECT m.id_menu, k.nama_kategori, m.nama_menu, m.harga, m.stok "
                   + "FROM tb_menu m JOIN tb_kategori k ON m.id_kategori = k.id_kategori";
        
        Connection conn = Koneksi.configDB();
        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);
        
        while (rs.next()) {
            model.addRow(new Object[]{
                no++, 
                rs.getString("id_menu"), 
                rs.getString("nama_kategori"), // Menampilkan nama, bukan ID
                rs.getString("nama_menu"), 
                rs.getString("harga"),
                rs.getString("stok")
            });
        }
        jTable1.setModel(model);
        
    } catch (Exception e) {
        System.out.println("Error tampil data menu: " + e.getMessage());
    }
}
    
