    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
    String kategoriTerpilih = jComboBox1.getSelectedItem().toString();
    String namaMenu = jTextField1.getText();
    String harga = jTextField2.getText();
    String stok = jTextField3.getText();
    
    // Validasi form kosong
    if (kategoriTerpilih.equals("- Pilih Kategori -") || namaMenu.equals("") || harga.equals("") || stok.equals("")) {
        JOptionPane.showMessageDialog(null, "Semua data (Kategori, Nama Menu, Harga, Stok) wajib diisi!");
        return;
    }
    
    java.sql.Connection conn = Koneksi.configDB();
    java.sql.Statement st = conn.createStatement();
    
    // 1. Cari tau id_kategori dari nama yang dipilih di JComboBox
    String sqlCariKategori = "SELECT id_kategori FROM tb_kategori WHERE nama_kategori = '" + kategoriTerpilih + "'";
    java.sql.ResultSet rs = st.executeQuery(sqlCariKategori);
    
    String idKategori = "";
    if (rs.next()) {
        idKategori = rs.getString("id_kategori");
    }
    
    // 2. Simpan data ke tb_menu menggunakan id_kategori yang baru didapatkan
    String sqlInsert = "INSERT INTO tb_menu (id_kategori, nama_menu, harga, stok) VALUES ('" + idKategori + "', '" + namaMenu + "', '" + harga + "', '" + stok + "')";
    st.executeUpdate(sqlInsert);
    
    JOptionPane.showMessageDialog(null, "Data Menu Berhasil Disimpan!");
    tampilkanData(); // Refresh JTable
    
    // Memanggil aksi tombol reset secara otomatis setelah simpan
    jButton4.doClick(); // Mengasumsikan jButton4 adalah tombol Reset kamu
    
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error Simpan: " + e.getMessage());
}
    }//GEN-LAST:event_jButton1ActionPerformed

