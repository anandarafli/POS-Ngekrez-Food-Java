    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        // Mengambil ID Kategori dari kolom index 1 (kolom kedua di tabel)
        idKategoriTerpilih = jTable1.getValueAt(baris, 1).toString(); 
        // Menampilkan Nama Kategori ke jTextField1 dari kolom index 2
        jTextField1.setText(jTable1.getValueAt(baris, 2).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
        String namaKategori = jTextField1.getText();

        if (namaKategori.equals("") || idKategoriTerpilih.equals("")) {
            JOptionPane.showMessageDialog(null, "Silakan pilih data pada tabel terlebih dahulu!");
            return;
        }

        String sql = "UPDATE tb_kategori SET nama_kategori = '" + namaKategori + "' WHERE id_kategori = '" + idKategoriTerpilih + "'";
        java.sql.Connection conn = Koneksi.configDB();
        java.sql.Statement st = conn.createStatement();
        st.executeUpdate(sql);

        JOptionPane.showMessageDialog(null, "Data Berhasil Diubah!");
        tampilkanData(); // Refresh JTable
        jTextField1.setText(""); // Kosongkan textfield
        idKategoriTerpilih = ""; // Reset ID temp

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            if (idKategoriTerpilih.equals("")) {
            JOptionPane.showMessageDialog(null, "Silakan pilih data pada tabel yang ingin dihapus!");
            return;
        }

        // Membuat konfirmasi pop-up sebelum menghapus
        int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            String sql = "DELETE FROM tb_kategori WHERE id_kategori = '" + idKategoriTerpilih + "'";
            java.sql.Connection conn = Koneksi.configDB();
            java.sql.Statement st = conn.createStatement();
            st.executeUpdate(sql);

            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus!");
            tampilkanData(); // Refresh JTable
            jTextField1.setText(""); // Kosongkan textfield
            idKategoriTerpilih = ""; // Reset ID temp
        }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

