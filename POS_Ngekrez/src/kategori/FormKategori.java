    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       try {
    // Menggunakan jTextField1 bawaan NetBeans
    String namaKategori = jTextField1.getText(); 
    
    if (namaKategori.equals("")) {
        JOptionPane.showMessageDialog(null, "Nama Kategori tidak boleh kosong!");
        return;
    }
    
    String sql = "INSERT INTO tb_kategori (nama_kategori) VALUES ('" + namaKategori + "')";
    java.sql.Connection conn = Koneksi.configDB();
    java.sql.Statement st = conn.createStatement();
    st.executeUpdate(sql);
    
    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan!");
    tampilkanData(); // Refresh tabel setelah simpan
    jTextField1.setText(""); // Kosongkan inputan
    
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, e.getMessage());
}
    }//GEN-LAST:event_jButton1ActionPerformed

