    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
    String nama = jTextField1.getText();
    String username = jTextField2.getText();
    String password = jTextField3.getText();
    
    if (idKasirTerpilih.equals("") || nama.equals("") || username.equals("") || password.equals("")) {
        JOptionPane.showMessageDialog(null, "Silakan pilih data pada tabel terlebih dahulu dan pastikan form tidak kosong!");
        return;
    }
    
    String sql = "UPDATE tb_kasir SET nama_kasir = '" + nama + "', username = '" + username + "', password = '" + password + "' WHERE id_kasir = '" + idKasirTerpilih + "'";
    java.sql.Connection conn = Koneksi.configDB();
    java.sql.Statement st = conn.createStatement();
    st.executeUpdate(sql);
    
    JOptionPane.showMessageDialog(null, "Data Kasir Berhasil Diubah!");
    tampilkanData(); // Refresh tabel
    jButton4.doClick(); // Panggil tombol reset
    
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error Edit: " + e.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
    if (idKasirTerpilih.equals("")) {
        JOptionPane.showMessageDialog(null, "Silakan pilih data pada tabel yang ingin dihapus!");
        return;
    }
    
    int konfirmasi = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin ingin menghapus data kasir ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    
    if (konfirmasi == JOptionPane.YES_OPTION) {
        String sql = "DELETE FROM tb_kasir WHERE id_kasir = '" + idKasirTerpilih + "'";
        java.sql.Connection conn = Koneksi.configDB();
        java.sql.Statement st = conn.createStatement();
        st.executeUpdate(sql);
        
        JOptionPane.showMessageDialog(null, "Data Kasir Berhasil Dihapus!");
        tampilkanData(); // Refresh tabel
        jButton4.doClick(); // Panggil tombol reset
    }
    
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error Hapus: " + e.getMessage());
}
    }//GEN-LAST:event_jButton3ActionPerformed

