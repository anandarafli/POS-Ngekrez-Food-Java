    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
    String nama = jTextField1.getText();
    String username = jTextField2.getText();
    String password = jTextField3.getText();
    
    // Validasi input tidak boleh kosong
    if (nama.equals("") || username.equals("") || password.equals("")) {
        JOptionPane.showMessageDialog(null, "Semua data (Nama, Username, Password) wajib diisi!");
        return;
    }
    
    String sql = "INSERT INTO tb_kasir (nama_kasir, username, password) VALUES ('" + nama + "', '" + username + "', '" + password + "')";
    java.sql.Connection conn = Koneksi.configDB();
    java.sql.Statement st = conn.createStatement();
    st.executeUpdate(sql);
    
    JOptionPane.showMessageDialog(null, "Data Kasir Berhasil Disimpan!");
    tampilkanData(); // Refresh tabel
    jButton4.doClick(); // Memanggil tombol reset otomatis
    
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error Simpan: " + e.getMessage());
}
    }//GEN-LAST:event_jButton1ActionPerformed

