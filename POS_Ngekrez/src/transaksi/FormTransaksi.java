private void hitungTotal() {
    grandTotal = 0;
    // Melakukan perulangan untuk membaca semua baris di tabel keranjang
    for (int i = 0; i < jTable1.getRowCount(); i++) {
        // Mengambil nilai dari kolom index 4 (Subtotal)
        grandTotal += Integer.parseInt(jTable1.getValueAt(i, 4).toString());
    }
    // Menampilkan total ke Label besar yang kamu buat
    jLabelTotal.setText("Rp. " + grandTotal); 
}

