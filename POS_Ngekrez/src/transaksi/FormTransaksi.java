    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
    String namaMenu = jComboBox1.getSelectedItem().toString();
    String harga = jTextField1.getText();
    String qty = jTextField2.getText();
    
    if (namaMenu.equals("- Pilih Menu -") || harga.equals("") || qty.equals("")) {
        JOptionPane.showMessageDialog(null, "Pilih menu dan masukkan jumlah beli (Qty)!");
        return;
    }
    
    // 1. Mencari id_menu dari database (dibutuhkan nanti untuk tabel detail_transaksi)
    String idMenu = "";
    java.sql.Connection conn = Koneksi.configDB();
    java.sql.Statement st = conn.createStatement();
    java.sql.ResultSet rs = st.executeQuery("SELECT id_menu FROM tb_menu WHERE nama_menu = '" + namaMenu + "'");
    if (rs.next()) {
        idMenu = rs.getString("id_menu");
    }
    
    // 2. Menghitung Subtotal (Harga x Qty)
    int subtotal = Integer.parseInt(harga) * Integer.parseInt(qty);
    
    // 3. Memasukkan data ke baris JTable (Keranjang)
    modelKeranjang.addRow(new Object[]{
        idMenu,
        namaMenu,
        harga,
        qty,
        String.valueOf(subtotal)
    });
    
    // 4. Update Grand Total
    hitungTotal();
    
    // 5. Bersihkan form input untuk pesanan berikutnya
    jComboBox1.setSelectedIndex(0);
    jTextField1.setText("");
    jTextField2.setText("");
    jTextField2.requestFocus();
    
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error masuk keranjang: " + e.getMessage());
}
    }//GEN-LAST:event_jButton1ActionPerformed

