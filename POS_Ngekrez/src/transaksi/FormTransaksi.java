    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {
    // 1. Validasi keranjang dan pembayaran
    if (modelKeranjang.getRowCount() == 0) {
        JOptionPane.showMessageDialog(null, "Keranjang masih kosong!");
        return;
    }
    
    int bayar = Integer.parseInt(jTextField3.getText());
    if (bayar < grandTotal) {
        JOptionPane.showMessageDialog(null, "Uang pembayaran kurang!");
        return;
    }
    
    Connection conn = Koneksi.configDB();
    
    // 2. Simpan ke tb_transaksi
    // Kita buat tanggal hari ini secara otomatis
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String tanggal = sdf.format(new Date());
    
    // Catatan: Untuk testing, kita asumsikan id_kasir = 1.
    String sqlTransaksi = "INSERT INTO tb_transaksi (id_kasir, tanggal_transaksi, total_bayar) VALUES ('2', '" + tanggal + "', '" + grandTotal + "')";
    
    // Statement khusus untuk mengambil ID transaksi yang baru saja digenerate oleh database
    Statement st = conn.createStatement();
    st.executeUpdate(sqlTransaksi, Statement.RETURN_GENERATED_KEYS);
    
    ResultSet rs = st.getGeneratedKeys();
    int idTransaksiBaru = 0;
    if (rs.next()) {
        idTransaksiBaru = rs.getInt(1); // Menyimpan ID transaksi baru (misal: Transaksi ke-15)
    }
    
    // 3. Simpan ke tb_detail_transaksi & Kurangi Stok secara berulang sebanyak isi keranjang
    for (int i = 0; i < jTable1.getRowCount(); i++) {
        String idMenu = jTable1.getValueAt(i, 0).toString();
        String qty = jTable1.getValueAt(i, 3).toString();
        String subtotal = jTable1.getValueAt(i, 4).toString();
        
        // Menyimpan riwayat setiap menu yang dibeli
        String sqlDetail = "INSERT INTO tb_detail_transaksi (id_transaksi, id_menu, qty, subtotal) VALUES ('" + idTransaksiBaru + "', '" + idMenu + "', '" + qty + "', '" + subtotal + "')";
        Statement stDetail = conn.createStatement();
        stDetail.executeUpdate(sqlDetail);
        
        // Mengurangi stok di tb_menu secara otomatis!
        String sqlUpdateStok = "UPDATE tb_menu SET stok = stok - " + qty + " WHERE id_menu = '" + idMenu + "'";
        Statement stStok = conn.createStatement();
        stStok.executeUpdate(sqlUpdateStok);
    }
    
    JOptionPane.showMessageDialog(null, "Transaksi Berhasil! Kembalian: Rp. " + jTextField4.getText());
    
    // 4. Reset Form untuk pelanggan berikutnya
    modelKeranjang.setRowCount(0); // Kosongkan tabel keranjang
    grandTotal = 0;
    jLabelTotal.setText("Rp. 0");
    jTextField3.setText("");
    jTextField4.setText("");
    
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error Transaksi: " + e.getMessage());
}
    }//GEN-LAST:event_jButton2ActionPerformed

